const getCoords = (locationName: string): { lat: number; lng: number } => {
    // Simple heuristic matching
    if (
        locationName.includes("Preah Vihear") ||
        locationName.includes("Koul 8")
    )
        return LOCATIONS["Preah Vihear"]
    if (locationName.includes("An Ses") || locationName.includes("Anses"))
        return LOCATIONS["An Ses"]
    if (locationName.includes("Tamone") || locationName.includes("Ta Moan"))
        return LOCATIONS.Tamone
    if (locationName.includes("Ta Krobey") || locationName.includes("Ta Thav"))
        return LOCATIONS["Ta Krobey"]
    if (locationName.includes("Khna")) return LOCATIONS.Khna
    if (locationName.includes("O Smach") || locationName.includes("O' Smach"))
        return LOCATIONS["O Smach"]
    if (locationName.includes("Phnom Chat") || locationName.includes("Techo"))
        return LOCATIONS["Phnom Chat"]
    if (locationName.includes("Sra Em")) return LOCATIONS["Sra Em"]
    if (
        locationName.includes("Thmar Puok") ||
        locationName.includes("Moho Srob") ||
        locationName.includes("Svay Chrum")
    )
        return LOCATIONS["Thmar Puok"]
    if (
        locationName.includes("Boeung Trakoun") ||
        locationName.includes("Boeung Trakuan")
    )
        return LOCATIONS["Boeung Trakoun"]
    if (
        locationName.includes("Prey Chan") ||
        locationName.includes("O Bei Chon") ||
        locationName.includes("O' Bei Choan")
    )
        return LOCATIONS["Prey Chan"]
    if (locationName.includes("Domnak Sdech")) return LOCATIONS["Domnak Sdech"]
    if (locationName.includes("Chakrey")) return LOCATIONS.Chakrey
    if (locationName.includes("Ta Sda")) return LOCATIONS["Ta Sda"]
    if (locationName.includes("Thmor Da") || locationName.includes("Ek Pheap"))
        return LOCATIONS["Thmor Da"]
    if (
        locationName.includes("O' Jik") ||
        locationName.includes("Ojik") ||
        locationName.includes("O Jik")
    )
        return LOCATIONS["O' Jik bridge"]
    if (
        locationName.includes("Srei Snam") ||
        locationName.includes("Srey Snam") ||
        locationName.includes("Siem Reap")
    )
        return LOCATIONS["Srei Snam"]

    // Default fallback center of border
    return { lat: 14.3, lng: 104.0 }
}

